
function add(a, b) {
    let adicao = a + b
    return adicao
}

function multiply(a, b) {
    let multiplicacao = a * b
    return multiplicacao
}

function divide(a, b) {
    let divisao = a / b
    return divisao
}

function mod(a, b) {
    let restoDaDivisao = a % b
    return restoDaDivisao
}

function exponent(a, b) {
    let potenciacao = a ** b
    return potenciacao
}

function subt(a, b) {
    let subtracao = a - b
    return subtracao
}


